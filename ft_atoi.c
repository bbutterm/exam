#include <stdio.h>
int ft_atoi(char* s)
{
	int sign,i,r,l;
	r = 0;
	sign = 1;
	i = 0;
	while (s[i] == '\t' || s[i] =='\n' || s[i] == '\v' || s[i] == '\f' || s[i] =='\r' || s[i] ==32)
	{
		i++;
	}
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
		{
			sign = -1;
		}
		i++;
	}
	while (s[i] >= '0' && s[i]<='9')
	{	
		r = r * 10 + s[i] - '0';
		i++;
	}
	return(r*sign);
}
#include <stdlib.h>
int main(){
	printf("%d\n",ft_atoi("999"));
	printf("%d\n",atoi("999"));
}
